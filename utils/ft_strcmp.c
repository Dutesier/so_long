#include "so_long.h"

int	ft_strcmp(char *one, char *two)
{
	int i;

	i = 0;
	while (one[i] != '\0' && two[i] != '\0')
	{
		if (one[i] != two[i])
			return (0);
		i++;
	}
	if (one[i] == '\0' && two[i] != '\0')
		return (0);
	if (one[i] != '\0' && two[i] == '\0')
		return (0);
	return (1);
}
